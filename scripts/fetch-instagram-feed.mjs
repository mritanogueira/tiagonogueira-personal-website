#!/usr/bin/env node
/**
 * Fetches latest posts from @tiagoamaralnogueira and writes js/instagram-feed-data.json.
 * Run before deploy: node scripts/fetch-instagram-feed.mjs
 */

import { execFile } from "node:child_process";
import { promisify } from "node:util";
import { writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const execFileAsync = promisify(execFile);

const USERNAME = "tiagoamaralnogueira";
const PROFILE_URL = `https://www.instagram.com/${USERNAME}/`;
const POST_COUNT = 9;
const IG_APP_ID = "936619743392459";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_JSON = join(__dirname, "..", "js", "instagram-feed-data.json");
const OUT_JS = join(__dirname, "..", "js", "instagram-feed-data.js");

const FETCH_HEADERS = {
  "User-Agent": "Instagram 219.0.0.12.117 Android",
  "X-IG-App-ID": IG_APP_ID,
  Accept: "*/*",
};

async function fetchJson(url) {
  const { stdout } = await execFileAsync(
    "curl",
    [
      "-sL",
      "-A",
      FETCH_HEADERS["User-Agent"],
      "-H",
      `X-IG-App-ID: ${IG_APP_ID}`,
      url,
    ],
    { maxBuffer: 10 * 1024 * 1024 }
  );
  return JSON.parse(stdout);
}

async function fetchLatestShortcodes() {
  const data = await fetchJson(
    `https://www.instagram.com/api/v1/users/web_profile_info/?username=${USERNAME}`
  );
  const edges =
    data?.data?.user?.edge_owner_to_timeline_media?.edges ?? [];
  return edges
    .slice(0, POST_COUNT)
    .map((edge) => edge?.node?.shortcode)
    .filter(Boolean);
}

async function fetchOEmbed(permalink) {
  const url = `https://www.instagram.com/api/v1/oembed/?url=${encodeURIComponent(permalink)}`;
  const data = await fetchJson(url);
  return {
    permalink,
    thumbnailUrl: data.thumbnail_url || null,
    title: (data.title || "").replace(/\s+/g, " ").trim(),
    authorName: data.author_name || USERNAME,
  };
}

async function main() {
  console.log(`Fetching latest ${POST_COUNT} posts from @${USERNAME}…`);
  const shortcodes = await fetchLatestShortcodes();
  if (!shortcodes.length) {
    throw new Error("No posts returned from Instagram profile API.");
  }

  const posts = [];
  for (const shortcode of shortcodes) {
    const permalink = `${PROFILE_URL}p/${shortcode}/`;
    try {
      const post = await fetchOEmbed(permalink);
      posts.push({ id: shortcode, ...post });
      console.log(`  ✓ ${shortcode}`);
    } catch (err) {
      console.warn(`  ✗ ${shortcode}: ${err.message}`);
    }
    await new Promise((r) => setTimeout(r, 200));
  }

  if (!posts.length) {
    throw new Error("Failed to fetch metadata for any post.");
  }

  const payload = {
    username: USERNAME,
    profileUrl: PROFILE_URL,
    fetchedAt: new Date().toISOString(),
    posts,
  };

  const json = `${JSON.stringify(payload, null, 2)}\n`;
  await writeFile(OUT_JSON, json, "utf8");
  await writeFile(
    OUT_JS,
    `/** Generated by npm run instagram:sync — do not edit */\nwindow.INSTAGRAM_FEED_DATA = ${JSON.stringify(payload)};\n`,
    "utf8"
  );
  console.log(`Wrote ${posts.length} posts to ${OUT_JSON} and ${OUT_JS}`);
}

main().catch((err) => {
  console.error(err.message || err);
  process.exit(1);
});
